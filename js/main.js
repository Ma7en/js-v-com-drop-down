/* start drop 1 */

let d1optionMenu = document.querySelector(".drop-1 .select-menu");
let d1selectBtn = document.querySelector(".drop-1 .select-btn");
let d1options = document.querySelectorAll(".drop-1 .option");
let d1sBtn_text = document.querySelector(".drop-1 .sBtn-text");

d1selectBtn.addEventListener("click", () => d1optionMenu.classList.toggle("active"));

d1options.forEach(option => {
    
    option.addEventListener("click", () => {
        let selectedOption = option.querySelector(".option-text").innerText;
        d1sBtn_text.innerText = selectedOption;
        d1optionMenu.classList.remove("active");
    });

});





